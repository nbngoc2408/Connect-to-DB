    public void loadData() {
        try{
            JavaApplication1 a = new JavaApplication1();
            Connection conn = a.GetJDBCConnection();
            int number;
            Vector row, column;
            column = new Vector();
            Statement st = conn.createStatement();
            //ResultSet rs = st.executeQuery("select nv.MANV, nv.HOTEN, nv.EMAIL, convert(varchar(max), nv.LUONG,2) as LUONG\n" +
//"	from NHANVIEN nv");
            ResultSet rs = st.executeQuery("exec SP_SEL_NHANVIEN");
            ResultSetMetaData metaData = rs.getMetaData();
            number = metaData.getColumnCount();
            for (int i = 1; i <= number; i++){
                column.add(metaData.getColumnName(i));
            }
            tbn.setColumnIdentifiers(column);
            while (rs.next()){
                row = new Vector();
                for (int i = 1; i <= number; i++){
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                jTable1.setModel(tbn);
            }
        } catch (Exception ex){
            System.out.println(ex.toString());
        }
    }
    
    


    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed
        // TODO add your handling code here:
        try{
            JavaApplication1 a = new JavaApplication1();
            Connection conn = a.GetJDBCConnection();
            PreparedStatement ps = conn.prepareStatement("exec SP_INS_NHANVIEN ?, ?, ?, ?, ?, ?");
            ps.setString(1, txtMaNV.getText());
            ps.setString(2, txtHoTen.getText());
            ps.setInt(4, Integer.parseInt(txtLuong.getText()));
            ps.setString(3, txtEmail.getText());
            ps.setString(5, txtTenDN.getText());
            ps.setString(6, txtMatKhau.getText());
            int chk = ps.executeUpdate();
            if (chk > 0){
                JOptionPane.showMessageDialog(this, "Them thanh cong!");
                tbn.setRowCount(0);
                loadData();
            }
            else {
                JOptionPane.showMessageDialog(this, "Them that bai!");
            }
        }
         catch (HeadlessException | NumberFormatException | SQLException e){
            e.printStackTrace();
    }//GEN-LAST:event_jButtonThemActionPerformed
    }
